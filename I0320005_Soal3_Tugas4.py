#konversi lbs ke kg,  1 lbs = 0,45 kg
berat_maksimum = 50*0.45
print('Berat maksimum dalam kg adalah ',berat_maksimum, 'kg')

a = 110
b = 49

jawaban_a = a < berat_maksimum
jawaban_b = b < berat_maksimum
print('Dengan berat lebih dari 110 kg:', jawaban_a)
print('Dengan berat 49 kg:', jawaban_b)